package modelo.pojo;

public class Alarma {

	private String puerto;
	private int valorLimite;
	private int valorPuerto;
	/**
	 * @return the puerto
	 */
	public String getPuerto() {
		return puerto;
	}
	/**
	 * @param puerto the puerto to set
	 */
	public void setPuerto(String puerto) {
		this.puerto = puerto;
	}
	/**
	 * @return the valorLimite
	 */
	public int getValorLimite() {
		return valorLimite;
	}
	/**
	 * @param valorLimite the valorLimite to set
	 */
	public void setValorLimite(int valorLimite) {
		this.valorLimite = valorLimite;
	}
	/**
	 * @return the valorPuerto
	 */
	public int getValorPuerto() {
		return valorPuerto;
	}
	/**
	 * @param valorPuerto the valorPuerto to set
	 */
	public void setValorPuerto(int valorPuerto) {
		this.valorPuerto = valorPuerto;
	}
	
	
}
